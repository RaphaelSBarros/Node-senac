/** FUP que solicite o peso de 5 pessoas e calcule a media; Imprima o resultado;
 * 
 * @param {*} num1 
 * @param {*} num2 
 * @param {*} num3 
 * @param {*} num4 
 * @param {*} num5 
 * @returns 
 */
function pessoas(num1, num2, num3, num4, num5){
    return (num1+num2+num3+num4+num5)/5;
}

module.exports = { pessoas };